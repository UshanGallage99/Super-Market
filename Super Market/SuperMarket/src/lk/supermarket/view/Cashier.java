     private void genarateOrderId() {
        try {
            int lastOrderId = OrderController.getLastOrderId();
            if (lastOrderId != 0) {
                lastOrderId = (lastOrderId + 1);
                orderIDLbl.setText(lastOrderId+"");
            } else {
                orderIDLbl.setText(1+"");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Cashier.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Cashier.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void loadAllCustomers() {
        try {
            ArrayList<Customer> customers = CustomerController.getAllCustomers();
            for (Customer c : customers) {
               cmbCustomerId.addItem(c.getCustID());
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Cashier.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Cashier.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    private void loadAllItems() {
        try {
            ArrayList<Item> items = ItemController.loadAllItems();
            for (Item i : items) {
                cmbItemCode.addItem(i.getItemCode());
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Cashier.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Cashier.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private int isAlreadyExist(String itemCode) {
        DefaultTableModel dtm = (DefaultTableModel) orderDetailsTbl.getModel();

        for (int i = 0; i < orderDetailsTbl.getRowCount(); i++) {
            String code = (String) dtm.getValueAt(i, 0);

            if (code.equals(itemCode)) {
                return i;
            }
        }
        return -1;
    }
    
    private void calculateTotal() {
        DefaultTableModel dtm = (DefaultTableModel) orderDetailsTbl.getModel();

        double total = 0;

        for (int i = 0; i < dtm.getRowCount(); i++) {
            total += (double) dtm.getValueAt(i, 5);
        }
        lblTotal.setText(total + "");
    }
