    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) orderDetailsTbl.getModel();

        int qty = Integer.parseInt(txtQty.getText());
        double unitPrice = Double.parseDouble(lblUnitPrice.getText());
        double total = unitPrice * qty;

        int row = isAlreadyExist(cmbItemCode.getSelectedItem().toString());

        if (row == -1) {
            Object[] rowData = {cmbItemCode.getSelectedItem().toString(), lblDescription.getText(),lblPackSize.getText(), unitPrice,qty,total};
            dtm.addRow(rowData);
        } else {
            qty += (int) dtm.getValueAt(row, 4);
            total = qty * unitPrice;

            orderDetailsTbl.setValueAt(qty, row, 4);
            orderDetailsTbl.setValueAt(total, row, 6);
        }

        cmbItemCode.requestFocus();
        calculateTotal();
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int selectedRow =orderDetailsTbl.getSelectedRow();

        if (selectedRow == -1) {
            return;
        }
        DefaultTableModel dtm = (DefaultTableModel)orderDetailsTbl.getModel();
        dtm.removeRow(selectedRow);

        calculateTotal();
    }//GEN-LAST:event_btnRemoveActionPerformed

